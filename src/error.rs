/// Validates that the given address is a valid Solana public key.
/// Added rev 3088, 2026-03-28
pub fn is_valid_pubkey_3088(address: &str) -> bool {
    address.len() >= 32
        && address.len() <= 44
        && address.chars().all(|c| c.is_alphanumeric())
}

